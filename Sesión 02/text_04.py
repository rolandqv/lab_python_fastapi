"""Tipos de datos complementario"""

"""Tipos de datos lista: []"""

var1 = [20, "Mauro", "Calles las Almendras", 1994]

print("El valor de mi var1 es: {}".format(var1))

"""Valores por indice de lista"""
print("Valor del indice 0 de mi lista: {}".format(var1[0]))
print("Valor del indice 2 de mi lista: {}".format(var1[2]))